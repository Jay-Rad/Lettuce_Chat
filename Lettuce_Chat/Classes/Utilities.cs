        // Remove trailing empty bytes in the buffer.
        public static byte[] TrimBytes(byte[] bytes)
        {
            // Loop backwards through array until the first non-zero byte is found.
            var firstZero = 0;
            for (int i = bytes.Length - 1; i >= 0; i--)
            {
                if (bytes[i] != 0)
                {
                    firstZero = i + 1;
                    break;
                }
            }
            if (firstZero == 0)
            {
                throw new Exception("Byte array is empty.");
            }
            // Return non-empty bytes.
            return bytes.Take(firstZero).ToArray();
        }

        public static void WriteToLog(Exception Ex)
        {
            lock (LogLock)
            {
                var di = Directory.CreateDirectory(Path.Combine(RootPath, "Data", "Logs", DateTime.Now.Year.ToString(), DateTime.Now.Month.ToString()));
                var exception = Ex;
                while (exception != null)
                {
                    var entry = new
                    {
                        Type = "Error",
                        Timestamp = DateTime.Now,
                        Message = exception.Message,
                        Source = exception.Source,
                        Stack = exception.StackTrace
                    };
                    File.WriteAllText(Path.Combine(di.FullName, DateTime.Now.Day.ToString() + ".txt"), JsonConvert.SerializeObject(entry) + Environment.NewLine);
                    exception = exception.InnerException;
                }
            }
        }

